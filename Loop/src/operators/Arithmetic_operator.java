package operators;

public class Arithmetic_operator {
	public static void main(String[] args) {
		int a=5;
		int b=8;
		
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a/b);
		System.out.println(a*b);
		System.out.println(a%b);
		
		int x=7;
		x++;
		
		int y=7;
		y--;
		 
		System.out.println(x);
		System.out.println(y);
	}

}
